impl TryFrom<usize> for KeyCode {
    type Error = ();
    fn try_from(item: usize) -> Result<Self, Self::Error> {
        match Self::from_u32(item as u32) {
            Some(kc) => Ok(kc),
            _ => Err(()),
        }
    }
}

impl From<u32> for KeyCode {
    fn from(item: u32) -> Self {
        Self::from_u32(item).expect(&format!("Invalid KeyCode: {}", item))
    }
}

impl From<KeyCode> for usize {
    fn from(item: KeyCode) -> Self {
        item as usize
    }
}

impl From<EventCode> for KeyCode {
    fn from(item: EventCode) -> Self {
        match item {
            EventCode::EV_KEY(evkey) => Self::from(evkey),
            _ => unreachable!(),
        }
    }
}

impl From<KeyCode> for EventCode {
    fn from(item: KeyCode) -> Self {
        let evkey = item.into();
        EventCode::EV_KEY(evkey)
    }
}

impl From<EV_KEY> for KeyCode {
    fn from(item: EV_KEY) -> Self {
        (item as u32).into()
    }
}

impl From<KeyCode> for EV_KEY {
    fn from(item: KeyCode) -> Self {
        evdev_rs::enums::int_to_ev_key(item as u32)
            .expect(&format!("Invalid KeyCode: {}", item as u32))
    }
}

// ------------------ KeyValue --------------------

#[derive(Copy, Clone, Debug, PartialEq, Eq)]
pub enum KeyValue {
    Release = 0,
    Press = 1,
    Repeat = 2,
}

impl From<i32> for KeyValue {
    fn from(item: i32) -> Self {
        match item {
            0 => Self::Release,
            1 => Self::Press,
            2 => Self::Repeat,
            _ => unreachable!(),
        }
    }
}

// ------------------ KeyEvent --------------------

pub struct KeyEvent {
    pub time: TimeVal,
    pub code: KeyCode,
    pub value: KeyValue,
}

impl KeyEvent {
    pub fn new(code: KeyCode, value: KeyValue) -> Self {
        let time = TimeVal::new(0, 0);
        Self { time, code, value }
    }

    #[cfg(test)]
    pub fn new_press(code: KeyCode) -> Self {
        Self::new(code, KeyValue::Press)
    }

    #[cfg(test)]
    pub fn new_release(code: KeyCode) -> Self {
        Self::new(code, KeyValue::Release)
    }
}

impl TryFrom<InputEvent> for KeyEvent {
    type Error = ();
    fn try_from(item: InputEvent) -> Result<Self, Self::Error> {
        match &item.event_type {
            EventType::EV_KEY => Ok(Self {
                time: item.time,
                code: item.event_code.into(),
                value: item.value.into(),
            }),
            _ => Err(()),
        }
    }
}

impl From<KeyEvent> for InputEvent {
    fn from(item: KeyEvent) -> Self {
        Self {
            time: item.time,
            event_type: EventType::EV_KEY,
            event_code: item.code.into(),
            value: item.value as i32,
        }
    }
}
